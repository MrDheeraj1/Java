public class Datatype{
 public static void main(String args[]){

     byte c =8;
     char ch = 'a' ;
     boolean var = false ;
     int a = 1;
     float f = 9.74444448f;
     long  b = 99999999999L ;
     double d = 9.9;
   
     // TO PRINT ALL THE VARIABLES

    
     System.out.println("byte c = " + c);
     System.out.println("char = " + ch);
     System.out.println("boolean var = " + var);
     System.out.println("int a = " + a);
     System.out.println("float f = " + f);
     System.out.println("long b = " + b);
     System.out.println("double d = " + d);


 }
 
 
}
/*  size of all data types 
        * byte - 1 byte
        * boolean - 1 bit
        * char - 2 byte
        * int - 4 byte
        * float - 4 byte
        * long - 8 byte
        * double - 8 byte
     */


/*  range of all data types
    byte - (-2^7) to (2^7)-1  i.e -128 to 127
    char - 0 to 65535
    boolean - true or false
    int - (-2^31) to (2^31)-1 i.e. -2147483648 to 2147483647
    float - 3.4e-038 to 3.4e+038
    long - (-2^63) to (2^63)-1 i.e. -9223372036854775808 to 9223372036854775807
    double - 1.7e-308 to 1.7e+308
 */