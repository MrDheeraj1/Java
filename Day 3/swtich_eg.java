public class swtich_eg {
    public static void main(String args[]){
        char ch = 'z';
        switch (ch) {
            case 'a': System.out.println("maggie");
            break;
            case 'b': System.out.println("coke");
             break;
            case 'c': System.out.println("biscuit");
             break;
            case 'd': System.out.println("munchies");
             break;
            default : System.out.println("we wake up");
        }

    }
}

// swich can be used for any datatype it can be float , int , character or other
