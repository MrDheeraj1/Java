public class logical_operator {
    public static void main(String args[]){
      System.out.println(3>1 && 5<2) ; // logical AND alltrue then true otherwise false
      System.out.println(3>1 || 5<2) ; // logical OR any one true then true otherwise false
      System.out.println(!(3>12)) ; // logical NOT reverse the result

    }
}